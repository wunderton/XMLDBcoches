   public int buscar(int id){
       int posicion = 0;
       boolean entrar = false;
        for(int i = 0;i <= coches.size() && entrar !=true ;i++){
            if(coches.get(i).getMatricula()== id){
              entrar = true;
              posicion = i;
            }
               
        }
        return posicion;
    }    
