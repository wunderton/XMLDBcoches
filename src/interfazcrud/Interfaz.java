   static void  CrearElemento(String datoEmple, String valor,Element raiz, Document document){

        Element elem = document.createElement(datoEmple); 
        Text text = document.createTextNode(valor); //damos valor
        raiz.appendChild(elem); //pegamos el elemento hijo a la raiz
        elem.appendChild(text); //pegamos el valor
    }
    
    public void NuevoFicheroCocheXML(){
         
    DocumentBuilderFactory factory = DocumentBuilderFactory.newInstance();

    try{
        
      DocumentBuilder builder = factory.newDocumentBuilder();
      DOMImplementation implementation = builder.getDOMImplementation();
      Document document = implementation.createDocument(null, "coches", null);
      document.setXmlVersion("1.0"); 
      
        for(int i = 0;i < coches.size();i++) {
          Element raiz = document.createElement("coches");  
          document.getDocumentElement().appendChild(raiz);
          CrearElemento("nombre", String.valueOf(coches.get(i).getNombre()), raiz, document); 
          CrearElemento("modelo", String.valueOf(coches.get(i).getModelo()), raiz, document); 
          CrearElemento("Matricula", Integer.toString(coches.get(i).getMatricula()), raiz, document); 
          CrearElemento("Kilometros", Integer.toString(coches.get(i).getkilometros()), raiz, document); 
      } 

      Source source = new DOMSource(document);
      Result result =  new StreamResult(new java.io.File("coche.xml"));        
      Transformer transformer = TransformerFactory.newInstance().newTransformer();
      transformer.transform(source, result);

     }catch(ParserConfigurationException | TransformerException | DOMException e){ } 
    
    }
